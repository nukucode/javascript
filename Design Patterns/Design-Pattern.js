//: Javascript Design Patterns

/* A design pattern is a term used in software engineering for general, reusable solution to a commonly occuring problem in software design. */

//* Design pattern are usually three major groups =>

//? Creational Design Pattern

/* As the name suggests, these patterns are for handling object creational mechanisms. A creational design pattern basically solves a problem by controlling the creation process of an object.

=> Constructor, Prototype, Factory, Singleton Patterns*/

//? Structural Design Pattern

/* These patterns are concerned with class and object composition. They help structure or restructure one or more parts without affecting entire system. In other words, they help obtain functionalities without tampering with the exiting ones.

=> Adapter, Composite, Decorator, Facade, Flyweight, Proxy Patterns*/

//? Behavioral Design Pattern

/* These patterns are concerned with improving communication between dissimilar objects.

=> Chain of Resposibility, Command, Iterator, Mediator, Observer, State Pattern, Strategy, Template Pattern.*/
